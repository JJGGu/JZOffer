    public static int minNumberInRotateArray(int [] array) {
        int left = 0;
        int right = array.length - 1;
        if (array[left] == array[right]){
            return array[left];
        }
        while (left < right){
            int mid = (left + right) >> 1;
            if (array[mid] > array[right]) {
                left = mid + 1;
            }
            else if (array[mid] < array[right]) {
                right = mid;
            }
            else {
                right--;
            }

        }
        return array[left];
    }

